var total = 20;
var counter = 2;

console.log("=== LOOPING PERTAMA ===");
while(counter <= total) {
  console.log(counter + " - I Love Coding");
  counter += 2;
}

console.log("=== LOOPING KEDUA ===");
while(total >= 2) {
  console.log(total + " - I will become fullstack developer");
  total -= 2;
}